'use strict';

var assert = require('power-assert');

var iso8601 = require('../../lib/util/iso8601.js');

describe('sort', function () {
  it('terms', function () {
    let given = [
      '2015-01-03T01:00Z/2015-01-03T02:00Z',
      '2015-01-02T09:00Z',
      '2015-01-02/2015-01-03',
      '2015-01-02T00:00Z/2015-01-02T01:00Z',
      '2015-01-02',
      '2015-01-01T09:00Z',
      '2015-01-01/2015-01-02',
      '2015-01-01T00:00Z/2015-01-01T01:00Z',
      '2015-01-01'
    ];

    let expected = [
      "2015-01-01",
      "2015-01-01T00:00Z/2015-01-01T01:00Z",
      "2015-01-01/2015-01-02",
      "2015-01-01T09:00Z",
      "2015-01-02",
      "2015-01-02T00:00Z/2015-01-02T01:00Z",
      "2015-01-02/2015-01-03",
      "2015-01-02T09:00Z",
      "2015-01-03T01:00Z/2015-01-03T02:00Z"
    ];

    let actual = given.sort(iso8601.compare);

    assert.deepEqual(actual, expected);
  });
});
